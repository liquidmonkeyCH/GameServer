#include "GameServer.hpp"
#include "ServiceManager.hpp"

namespace Utility
{
////////////////////////////////////////////////////////////////////////////////////////////////////
namespace main
{
////////////////////////////////////////////////////////////////////////////////////////////////////
bool 
GameServer::OnStart(void)
{
	m_io_service.start();
	m_dispatch_service.start(1);

	m_kGameServer.init(5000, &m_io_service, &m_dispatch_service);
	m_kGameServer.start("127.0.0.1", 55552);


	return true;
}
////////////////////////////////////////////////////////////////////////////////////////////////////
void 
GameServer::OnStop(void)
{
	m_kGameServer.stop();



	m_io_service.stop();
	m_dispatch_service.stop();
}
////////////////////////////////////////////////////////////////////////////////////////////////////
}//namespace main
////////////////////////////////////////////////////////////////////////////////////////////////////
}//namespace Utility 
